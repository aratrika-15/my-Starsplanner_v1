package stars;

/**
 * Enum of the course type of all courses
 */
public enum CourseType {
    /**
     * The course of this type has lectures, tutorials and labs
     */
    LEC_TUT_LAB,
    /**
     * A course of this type has only lectures and tutorials
     */
    LEC_TUT,
    /**
     * A course of this type has only lectures
     */
    LEC
}
